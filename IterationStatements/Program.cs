// while
int a = 1;
while (a < 5)
{
    Console.WriteLine($"while: {a}");
    a++;
}

Console.WriteLine();
// do-while
int b = 1;
do
{
    Console.WriteLine($"do-while: {b}");
    b++;
} while (b < 1);

Console.WriteLine();
// for 
for (int i = 1; i < 5; i++)
{
    Console.WriteLine($"for: {i}");
}